      txt_facilityNo.setEditable(false);
      txt_facilityNo.setText(tbl_URVehicles.getValueAt(tbl_URVehicles.getSelectedRow(), 0).toString());
      txt_vehicleNo.setText(tbl_URVehicles.getValueAt(tbl_URVehicles.getSelectedRow(), 1).toString());
