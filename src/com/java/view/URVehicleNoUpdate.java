  private void txt_facilityNoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_facilityNoActionPerformed
    if (CommonUtils.facilityNoValidator(txt_facilityNo.getText().trim().toUpperCase())) {
      String facNo = null, VehiNo = null;
      for (FileEntity file : files) {
        if (file.getFacilityNo().equals(txt_facilityNo.getText().trim().toUpperCase())) {
          facNo = file.getFacilityNo();
          VehiNo = file.getVehicleNo();
        }
      }
      if (facNo != null) {
        txt_vehicleNo.setText(VehiNo);
        txt_facilityNo.nextFocus();
      } else {
        JOptionPane.showMessageDialog(this, "Please enter valid facility number.", "Not Found", JOptionPane.WARNING_MESSAGE);
      }
    } else {
      JOptionPane.showMessageDialog(this, "Please enter valid facility number.", "Warning", JOptionPane.WARNING_MESSAGE);
    }
  }//GEN-LAST:event_txt_facilityNoActionPerformed

