  private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
    if (CommonUtils.vehicleNoValidator(txt_vehicleNo.getText().trim().toUpperCase())) {
      int success = 0;
      try {
        success = controller.update(txt_facilityNo.getText().trim().toUpperCase(), txt_vehicleNo.getText().trim().toUpperCase());
      } catch (ClassNotFoundException | SQLException ex) {
        JOptionPane.showMessageDialog(this, "Database connection not found.", "Error", JOptionPane.ERROR_MESSAGE);
        Logger.getLogger(URVehicleNoUpdate.class.getName()).log(Level.SEVERE, null, ex);
      }
      if (success == 1) {
        JOptionPane.showMessageDialog(this, "Data saved successfuly.", "Success", JOptionPane.INFORMATION_MESSAGE);
        btn_clearActionPerformed(null);
        try {
          controller.loadAllData();
        } catch (ClassNotFoundException | SQLException ex) {
          Logger.getLogger(URVehicleNoUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
      } else {
        JOptionPane.showMessageDialog(this, "Error on data save, Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
      }
    } else {
      JOptionPane.showMessageDialog(this, "Please enter valid vehicle number.", "Warning", JOptionPane.WARNING_MESSAGE);
    }
  }//GEN-LAST:event_btn_updateActionPerformed

